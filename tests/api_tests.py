class MockWindow:
    """
    Mock window class to capture evaluate_js calls and record events.
    """
    def __init__(self):
        self.events = []

    def evaluate_js(self, js_code: str) -> None:
        """
        Parses custom webview events dispatched by the backend.
        
        Args:
            js_code (str): The raw JavaScript code dispatched to webview.
        """
        # Match event name and details dict using regex
        match = re.search(r"CustomEvent\('([^']+)',\s*\{detail:\s*(.+)\}\)\);", js_code)
        if match:
            event_name = match.group(1)
            data_str = match.group(2)
            try:
                # Safely evaluate Python dict representation generated by f-string data serialization
                data = ast.literal_eval(data_str)
                self.events.append((event_name, data))
            except Exception:
                self.events.append((event_name, data_str))


