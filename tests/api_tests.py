    def test_download_binaries_completed(self) -> None:
        """
        Tests the asynchronous downloader starts and reports status correctly.
        """
        api = API()
        window = MockWindow()
        api.set_window(window)

        res = api.download_binaries()
        self.assertEqual(res, {"status": "download_started"})

        # Wait for download thread to terminate
        timeout = 5.0
        start_time = time.time()
        while time.time() - start_time < timeout:
            if any(e[0] == "download_complete" for e in window.events):
                break
            time.sleep(0.1)

        complete_events = [e for e in window.events if e[0] == "download_complete"]
        self.assertTrue(len(complete_events) > 0)
        # Verify it completes without crashing
        self.assertIn(complete_events[0][1]["status"], ["all_installed", "success"])

