    def _wait_for_conversion(self, window: MockWindow, timeout: float = 30.0) -> None:
        """
        Helper method to wait for async batch conversions to finish.
        
        Args:
            window (MockWindow): The mocked window object recording events.
            timeout (float): Max wait time in seconds.
        """
        start_time = time.time()
        while time.time() - start_time < timeout:
            # Check if queue execution is completed
            if any(e[0] == "batch_complete" for e in window.events):
                return
            
            # Fail immediately if any processing errors are reported
            err_events = [e for e in window.events if e[0] in ("batch_file_error", "batch_error")]
            if err_events:
                self.fail(f"Conversion queue failed with event: {err_events[0]}")
                
            time.sleep(0.1)
        self.fail("Conversion task timed out")

    def test_get_supported_formats(self) -> None:
        """
        Tests that get_supported_formats returns valid format lists for each category.
        """
        api = API()
        formats = api.get_supported_formats()
        
        self.assertIsInstance(formats, dict)
        for category in ConversionCategory:
            self.assertIn(category.value, formats)
            self.assertIsInstance(formats[category.value], list)
            self.assertTrue(len(formats[category.value]) > 0)
            self.assertTrue(all(isinstance(f, str) for f in formats[category.value]))

    def test_config_management(self) -> None:
        """
        Tests configuration exists, read, and write operations using a mock file path.
        """
        # Create a temporary config file path
        fd, temp_config_path = tempfile.mkstemp(suffix=".json", prefix="test_config_")
        os.close(fd)
        
        # Override the get_config_path function pointer
        original_get_config_path = backend.config._get_config_path_fn
        backend.config._get_config_path_fn = lambda: temp_config_path
        
        try:
            # Delete temporary file to test initial state where file is missing
            if os.path.exists(temp_config_path):
                os.remove(temp_config_path)

            api = API()
            
            # 1. Test config_exists initially false
            self.assertFalse(api.config_exists())

            # 2. Test read_config auto-creates config with defaults
            config = api.read_config()
            self.assertEqual(config, {"lang": "es", "theme": "dracula"})
            self.assertTrue(api.config_exists())

            # 3. Test update_config updates preferences
            api.update_config(lang="en", theme="nord")
            updated_config = api.read_config()
            self.assertEqual(updated_config, {"lang": "en", "theme": "nord"})
            
        finally:
            # Cleanup config override and temporary file
            backend.config._get_config_path_fn = original_get_config_path
            if os.path.exists(temp_config_path):
                os.remove(temp_config_path)

